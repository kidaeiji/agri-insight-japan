function splitActualForecast(data) {
  const idx = FORECAST_START_IDX;
  const actual = [...data.slice(0, idx + 1), ...Array(data.length - idx - 1).fill(null)];
  const forecast = [...Array(idx).fill(null), data[idx], ...data.slice(idx + 1)];
  return { actual, forecast };
}

function calcSupplyDemand(crop) {
  return ALL_YEARS.map((y, i) => ({
    year: y,
    supply: crop.production[i] + crop.imports[i],
    demand: crop.consumption[i],
    gap: (crop.production[i] + crop.imports[i]) - crop.consumption[i],
    isForecast: i > FORECAST_START_IDX
  }));
}

function calcRisk(crop) {
  const latestSS = crop.selfSufficiency[FORECAST_START_IDX];
  const forecast2035SS = crop.selfSufficiency[crop.selfSufficiency.length - 1];
  const farmerDecline = ((crop.farmers[FORECAST_START_IDX] - crop.farmers[crop.farmers.length - 1]) / crop.farmers[FORECAST_START_IDX] * 100).toFixed(0);
  const priceChange = ((crop.retailPrice[FORECAST_START_IDX] / crop.retailPrice[0] - 1) * 100).toFixed(0);

  let score = 0;
  if (latestSS < 20) score += 3;
  else if (latestSS < 50) score += 2;
  else if (latestSS < 80) score += 1;

  if (Math.abs(farmerDecline) > 40) score += 2;
  else if (Math.abs(farmerDecline) > 20) score += 1;

  const gapRatio = (crop.production[FORECAST_START_IDX] + crop.imports[FORECAST_START_IDX]) / crop.consumption[FORECAST_START_IDX];
  if (gapRatio < 0.95) score += 2;
  else if (gapRatio < 1.0) score += 1;

  return {
    score,
    level: score >= 4 ? 'high' : score >= 2 ? 'medium' : 'low',
    selfSufficiency: latestSS,
    farmerDecline: farmerDecline + '%',
    priceChange: '+' + priceChange + '%',
    selfSufficiencyRisk: latestSS < 20 ? 'high' : latestSS < 80 ? 'medium' : 'low',
    farmerRisk: Math.abs(farmerDecline) > 40 ? 'high' : Math.abs(farmerDecline) > 20 ? 'medium' : 'low',
    priceRisk: priceChange > 50 ? 'high' : priceChange > 20 ? 'medium' : 'low',
    gapRisk: gapRatio < 0.95 ? 'high' : gapRatio < 1.0 ? 'medium' : 'low'
  };
}
